package jahnavi;

public class assingment {

	public static void main(String[] args) {
		int a = 5;
		int var;
		var = a;
		System.out.println("var using =:" + var);
		var += a;
		System.out.println("var using +:=" + var);
		var -= a; 
		System.out.println("var using -=:" + var);
		var *= a;
		System.out.println("var using *=:" + var);
		var /= a;
		System.out.println("var using /=:" + var);
		var %= a;
		System.out.println("var using %=:" + var);
		
				
				
				
				

	}

}
