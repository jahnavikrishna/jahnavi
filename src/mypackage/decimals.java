package jahnavi;

public class decimals {

	public static void main(String[] args) {
		//declare variables
		double a = 6.2, b = 3.2;
		System.out.println( " a + b =" + ( a + b ));
		System.out.println( " a - b =" + ( a - b ));
		System.out.println( " a * b =" + ( a * b ));
		System.out.println( " a / b =" + ( a / b ));
		System.out.println( " a % b =" + ( a % b ));
		
		
		
		

	}

}
